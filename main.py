    # –ü–æ–ª—É—á–µ–Ω–∏–µ id –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è.
    photo_id = message.photo[-1].file_id 
    # –ü–æ–ª—É—á–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–∏.
    file_info = bot.get_file(photo_id) 
