    bot.send_message(message.chat.id, f'–í –∫–æ–ª–ª–µ–∫—Ü–∏—é –¥–æ–±–∞–≤–ª–µ–Ω–æ {number_files} –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π.')
    
   

